const fs = require('fs')
const path = require('path')

const rootDir = path.join(__dirname, '..')
const moduleDir = path.join(rootDir, 'module')
const outputFile = path.join(rootDir, 'public', '_worker.js')

const specialRoutes = {
  'daily_signin.js': '/daily_signin',
  'fm_trash.js': '/fm_trash',
  'personal_fm.js': '/personal_fm',
}

function parseRoute(fileName) {
  return (
    specialRoutes[fileName] ||
    `/${fileName.replace(/\.js$/i, '').replace(/_/g, '/')}`
  )
}

function toImportPath(fileName) {
  return `../module/${fileName}`.replace(/\\/g, '/')
}

function generateWorker() {
  const files = fs
    .readdirSync(moduleDir)
    .reverse()
    .filter((file) => file.endsWith('.js'))

  const imports = files
    .map((file, index) => `import mod${index} from '${toImportPath(file)}'`)
    .join('\n')

  const moduleDefs = files
    .map((file, index) => {
      const identifier = file.split('.').shift() || ''
      return [
        '  {',
        `    identifier: ${JSON.stringify(identifier)},`,
        `    route: ${JSON.stringify(parseRoute(file))},`,
        `    module: mod${index},`,
        '  },',
      ].join('\n')
    })
    .join('\n')

  return `/* eslint-disable */
// This file is generated by scripts/generate-pages-worker.js.
import { createServer } from 'node:http'
import { httpServerHandler } from 'cloudflare:node'
import serverModule from '../server.js'
import generateConfig from '../generateConfig.js'
import runtimeState from '../util/runtime-state.js'
${imports}

const { constructServer } = serverModule

const moduleDefs = [
${moduleDefs}
]

const apiRoutes = new Set(moduleDefs.map(({ route }) => route))
let handlerPromise = null

function normalizePath(pathname) {
  if (pathname.length > 1 && pathname.endsWith('/')) {
    return pathname.slice(0, -1)
  }
  return pathname
}

function syncRuntimeEnv(env = {}) {
  process.env.CLOUDFLARE_PAGES = 'true'
  for (const [key, value] of Object.entries(env)) {
    if (typeof value === 'string') {
      process.env[key] = value
    }
  }
  if (typeof env.NETEASE_ANONYMOUS_TOKEN === 'string') {
    runtimeState.setAnonymousToken(env.NETEASE_ANONYMOUS_TOKEN)
  }
  if (typeof env.XEAPI_PUBLIC_KEY === 'string') {
    try {
      runtimeState.setXeapiPublicKey(JSON.parse(env.XEAPI_PUBLIC_KEY))
    } catch (error) {
      console.error('Invalid XEAPI_PUBLIC_KEY:', error.message)
    }
  }
}

async function getHandler(env) {
  syncRuntimeEnv(env)
  if (!handlerPromise) {
    handlerPromise = (async () => {
      await generateConfig()
      const app = await constructServer(moduleDefs)
      return httpServerHandler(createServer(app))
    })()
  }
  return handlerPromise
}

function shouldHandleApi(request) {
  const { pathname } = new URL(request.url)
  return apiRoutes.has(normalizePath(pathname))
}

export default {
  async fetch(request, env, ctx) {
    if (!shouldHandleApi(request)) {
      return env.ASSETS.fetch(request)
    }

    const handler = await getHandler(env)
    return handler.fetch(request, env, ctx)
  },
}
`
}

fs.writeFileSync(outputFile, generateWorker())
console.log(`Generated ${path.relative(rootDir, outputFile)}`)
